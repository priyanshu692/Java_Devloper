package java_lang_package;
import java.lang.Math;
public class Math_class {
    public static void main(String[] args) {
        System.out.println("absolute value");
        System.out.println(Math.abs(-123));
        System.out.println("absolute value");
        System.out.println(Math.abs(123));

        System.out.println("Squere root");
        System.out.println(Math.sqrt(123));

        System.out.println("cube root");
        System.out.println(Math.cbrt(3));

        System.out.println("extract value decrement");
        System.out.println(Math.decrementExact(45));

    }
}
