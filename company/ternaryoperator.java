package company;

public class ternaryoperator {
    public static void main(String[] args) {
        int a=23; int b=33;
        //Max= (a>b);

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a!=b);
        System.out.println(a==b);
    }
}
