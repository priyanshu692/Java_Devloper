package company;

public class assignmentoperator {
    public static void main(String[] args) {
        int a=21;
        int b=22;
        a++;//21+1=22
        System.out.println(a);
        a+=11;//22+11=33
        System.out.println(a);
        a-=3;//33-3=30
        System.out.println(a);
        b%=3;//22%3=1
        System.out.println(b);
        a/=3;//30/3=10
        System.out.println(a);
        a*=3;//10*3=30
        System.out.println(a);
        int c=4;
        c^=6;
         System.out.println(c);
        a>>=3;
        System.out.println(a);
        a<<=3;
        System.out.println(a);
    }
}
